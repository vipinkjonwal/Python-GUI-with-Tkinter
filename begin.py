from tkinter import *
root = Tk()
#Line 2 will create a box which is first thing we will be needing.
theLabel = Label(root,text="This is Tkinter")
theLabel.pack()
#pack() - line 5 -  method is used to put widgets inside the window.
root.mainloop()
#mainloop() - line 7 - is used to display window on the screen.
